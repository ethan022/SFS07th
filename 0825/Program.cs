// 📌 블러링(Blurring)
// - 이미지를 부드럽게 만드는 기법
// - 노이즈 제거, 디테일 감소, 전처리 단계에서 활용
// - 주변 픽셀들의 평균값으로 대체 → 급격한 변화 완만하게
//
// 📌 가우시안 블러 (Gaussian Blur)
// - 가장 자연스럽고 널리 사용되는 블러링
// - 가우시안 분포 기반의 가중 평균
// - 중앙에 가까울수록 높은 가중치 → 자연스러운 블러 효과
// - 엣지 보존 효과가 있어 객체 인식 전처리에 적합
//
// 📌 박스 필터 (Box Filter)
// - 가장 단순한 블러링 방식 (단순 평균)
// - 모든 픽셀에 동일한 가중치 적용
// - 계산이 매우 빠르지만, 블러 품질은 가우시안보다 떨어짐
//
// 📌 메디안 필터 (Median Filter)
// - 노이즈 제거(특히 소금-후추 노이즈)에 특화된 필터
// - 주변 픽셀들의 "평균"이 아닌 "중간값" 사용
// - 엣지를 잘 보존하면서 노이즈 제거 효과 탁월
//
// 📌 양방향 필터 (Bilateral Filter)
// - 거리 + 색상 차이를 모두 고려하는 필터
// - 평탄한 영역만 블러링, 엣지는 보존
// - 사진 보정에 많이 활용됨
// - 계산 시간이 상대적으로 오래 걸림
